#include<stdio.h>
void main(){
    int x=2; // value of x can be anything
    switch (x)
    {
    case 1:
    printf("Number 1");
    break;
    case 2:
    printf("Number 2");
    break;
    case 3:
    printf("Number 3");
    break;
    default:
    printf("No number between 1 and 3");
    }
}